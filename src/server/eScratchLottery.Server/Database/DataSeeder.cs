        public static void SeedDemo(ScratchLotteryDbContext dbContext)
        {
            IEnumerable<Card> cards = Enumerable.Range(1, 1000)
                .Select((id) =>
                {
                    DemoPrices.TryGetValue(id, out string price);

                    return new Card
                    {
                        Name = $"Card - {id}",
                        Price = price
                    };
                });

            dbContext.AddRange(cards);
            dbContext.SaveChanges();
        }

